.MODEL SMALL 
.DATA
    PORTA EQU 0E880H
    PORTC EQU 0E882H
    CR EQU 0E883H
    FIRE DB 79H,50H,06H,71H,0,0
    HELP DB 73H,38H,79H,76H,0,0

.CODE
    MOV AX,@DATA
    MOV DS,AX
    MOV AL,80H
    MOV DX,CR
    OUT DX,AL
    MOV CX,2
AGAIN:
    MOV DI,20
DISP1:
    LEA SI,FIRE
    CALL DISPLAY
    DEC DI
    JNZ DISP1
    MOV DI,20
DISP2:
    LEA SI,HELP
    CALL DISPLAY
    DEC DI
    JNZ DISP2
    LOOP AGAIN
    MOV AH,4CH
    INT 21H

DISPLAY PROC
    MOV AH,0
BACK: 
    MOV AL,AH 
    MOV DX,PORTC
    OUT DX,AL
    LODSB
    MOV DX,PORTA
    OUT DX,AL
    CALL DELAY
    INC AH
    CMP AH,4
    JNZ BACK
    RET
DISPLAY ENDP

DELAY PROC
    PUSH BX
    PUSH CX 
    MOV BX,0FFFH
L1: MOV CX,0FFFH
L2: LOOP L2
    DEC BX 
    JNZ L1
    POP CX
    POP BX
    RET
DELAY ENDP
END