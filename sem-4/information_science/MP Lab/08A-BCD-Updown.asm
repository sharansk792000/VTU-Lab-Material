.MODEL SMALL
.DATA
    PORTA EQU 0E880H
    CR EQU 0E883H

.CODE

START:  
    MOV AX,DATA
    MOV DS,AX
    MOV AL,82H
    MOV DX,CR
    OUT DX,AL
    MOV AL,00H
    MOV DX,PORTA
UP:
    OUT DX,AL
    CALL DELAY
    INC AL
    CMP AL,99H
    JNE UP
    MOV AL,99H
    MOV DX,PORTA
DOWN:
    OUT DX,AL
    CALL DELAY
    DEC AL
    CMP AL,0FFH
    JNE DOWN
    MOV AH,4CH
    INT 21H

DELAY PROC
    MOV BX 0F000H
L1:
    MOV CX,0FFFFH
L2:
    LOOP L2
    DEC BX
    JNZ L1
    RET
DELAY ENDP
END
