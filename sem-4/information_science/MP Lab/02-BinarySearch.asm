.MODEL SMALL

DISPLAY MACRO MSG
	LEA DX,MSG
	MOV AH,09H
	INT 21H
	ENDM
.DATA
	A DW 1,2,3,4,5
	N DW 5
	KEY EQU 7
	MSG1 DB "NOT FOUND$"
	MSG2 DB "FOUND$"
.CODE
	MOV AX,@DATA
	MOV DS,AX
	MOV BX,00H
	MOV CX,N

AGAIN:	CMP BX,CX
	JG NFOUND
	MOV AX,BX
	ADD AX,CX
	SHR AX,01H
	MOV SI,AX
	ADD SI,SI
	CMP A[SI],KEY
	JE FOUND
	JC SETLB
	DEC AX
	MOV CX,AX
	JMP AGAIN

SETLB:	INC AX
	MOV BX,AX
	JMP AGAIN

FOUND:	DISPLAY MSG2
	JMP QUIT

NFOUND:	DISPLAY MSG1
	
QUIT:	MOV AH,4CH
	INT 21H
	END