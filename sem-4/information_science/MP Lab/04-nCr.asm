.MODEL SMALL

.DATA
	N	DW	5
	R	DW	2
	RESULT	DW	0

.CODE
	MOV AX,@DATA
	MOV DS,AX
	MOV AX,N
	MOV BX,R
	CALL NCR
	INT 3H

NCR PROC
	CMP BX,0
	JE RES1
	CMP BX,AX
	JE RES1
	CMP BX,1
	JE RESN
	DEC AX
	CMP BX,AX
	JE INCR 
	PUSH AX
	PUSH BX
	CALL NCR
	POP BX
	POP AX
	DEC BX
	PUSH AX
	PUSH BX
	CALL NCR
	POP BX
	POP AX
	RET

RES1:	INC RESULT
	RET

INCR:	INC RESULT
	
RESN: 	ADD RESULT,AX
	RET
NCR ENDP
	END
