.MODEL SMALL

DISPLAY MACRO MSG
	LEA DX,MSG
	MOV AH,09H
	INT 21H
	ENDM

.DATA
	STR DB "MAM"
	LEN DW LEN-STR
	RSTR DB LEN-STR DUP(0)
	MSG1 DB "PALINDROME$"
	MSG2 DB "NOT A PALINDROME$"

.CODE
	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX
	LEA SI,LEN-1
	LEA DI,RSTR
	MOV CX,LEN

BACK:	MOV AL,[SI]
	MOV [DI],AL
	DEC SI
	INC DI
	LOOP BACK
	CLD
	LEA SI,STR
	LEA DI,RSTR
	MOV CX,LEN
	REPE CMPSB
	JE PALIN
	DISPLAY MSG2
	JMP QUIT

PALIN:	DISPLAY MSG1
QUIT: 	MOV AH,4CH
	INT 21H
	END
	
	

	